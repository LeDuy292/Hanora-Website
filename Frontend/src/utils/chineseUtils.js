// Check if a character is Chinese (Hanzi)
export function isChinese(char) {
  return /[\u4e00-\u9fa5]/.test(char);
}

// Check if a string is punctuation or whitespace
export function isPunctuationOrSpace(str) {
  return /^[^\u4e00-\u9fa5a-zA-Z0-9\s]+$/.test(str) || /^\s+$/.test(str);
}

/**
 * Chinese Text Segmentation using Maximum Matching algorithm.
 * Tokenizes Chinese sentences into dictionary words.
 * Non-Chinese elements are preserved as separate tokens.
 *
 * @param {string} text - The input Chinese text
 * @returns {Array<object>} - List of word tokens
 */
export function segmentChineseText(text) {
  if (!text) return [];
  
  const tokens = [];
  let i = 0;
  const maxWordLength = 6;
  const len = text.length;
  
  while (i < len) {
    const char = text[i];
    
    // If not Chinese, consume as continuous non-Chinese blocks (whitespace, numbers, English words)
    if (!isChinese(char)) {
      let nonChineseBlock = char;
      let j = i + 1;
      while (j < len && !isChinese(text[j])) {
        // Stop on punctuation or space boundary if we want to segment them, or just merge
        if (isPunctuationOrSpace(text[j]) !== isPunctuationOrSpace(char)) {
          break;
        }
        nonChineseBlock += text[j];
        j++;
      }
      tokens.push({
        text: nonChineseBlock,
        isWord: false,
        isPunctuation: isPunctuationOrSpace(nonChineseBlock)
      });
      i = j;
      continue;
    }
    
    // Chinese maximum matching lookup
    let matched = false;
    for (let wordLen = Math.min(maxWordLength, len - i); wordLen >= 1; wordLen--) {
      const chunk = text.substring(i, i + wordLen);
      
      if (CHINESE_DICTIONARY[chunk]) {
        tokens.push({
          text: chunk,
          isWord: true,
          pinyin: CHINESE_DICTIONARY[chunk].pinyin,
          translation: CHINESE_DICTIONARY[chunk].translation,
          hsk: CHINESE_DICTIONARY[chunk].hsk
        });
        i += wordLen;
        matched = true;
        break;
      }
    }
    
    // Fallback: If no dictionary match, segment as single character
    if (!matched) {
      const fallbackPinyin = CHARACTER_DATABASE[char]?.pinyin || "";
      const fallbackTranslation = CHARACTER_DATABASE[char]?.translation || "Unknown character";
      
      tokens.push({
        text: char,
        isWord: true,
        pinyin: fallbackPinyin,
        translation: fallbackTranslation,
        hsk: 1, // Default HSK 1 fallback
        isFallback: true
      });
      i++;
    }
  }
  
  return tokens;
}

/**
 * Splits paragraph text into sentences.
 *
 * @param {string} text - Paragraph of Chinese text
 * @returns {Array<string>} - Array of sentences
 */
export function splitSentences(text) {
  if (!text) return [];
  // Split on Chinese and English periods, question marks, and exclamation marks, keeping the punctuations
  const sentenceEndings = /([^。？！?!]+[。？！?!]?)/g;
  const matches = text.match(sentenceEndings);
  return matches ? matches.map(s => s.trim()).filter(Boolean) : [text];
}
